
// Arithmetic Operators

let a = 10, b = 5;
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** 2);

//Assignment Operators

let x = 10;
x += 5;  
console.log(x); 
x -= 3;  
console.log(x); 
x *= 2;  
console.log(x); 
x /= 4;  
console.log(x);

//Comparision Operators

let m = 10, n = 5;
console.log(m > n);
console.log(m < n);
console.log(m == n);
console.log(m === 10);
console.log(m != n);
console.log(m >= 10);
console.log(n <= 5);

//Logocal Operators

let z = true, y = false;
console.log(z && y); 
console.log(z || y); 
console.log(!z);     

//Bitwise Operators

let o = 5, p = 1;
console.log(o & p);  
console.log(o | p);  
console.log(o ^ p);  
console.log(~o);     
console.log(o << 1); 
console.log(o >> 1); 

//String Operators

let str1 = "Hello, ";
let str2 = "World!";
console.log(str1 + str2); // "Hello, World!"

//Ternary Operators

let age = 18;
let res = (age >= 18) ? "Adult" : "Minor";
console.log(res);

//Type Operators

let s = "JavaScript";
console.log(typeof s); 
console.log(typeof 42); 
